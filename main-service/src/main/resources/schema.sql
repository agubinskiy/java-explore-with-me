CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  name VARCHAR(250) NOT NULL,
  email VARCHAR(254) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS locations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  lat FLOAT NOT NULL,
  lon FLOAT NOT NULL
);

CREATE TABLE IF NOT EXISTS categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  name VARCHAR(50) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  annotation VARCHAR(2000) NOT NULL,
  category_id BIGINT NOT NULL REFERENCES categories ON DELETE CASCADE,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  description VARCHAR(7000) NOT NULL,
  event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  initiator_id BIGINT NOT NULL REFERENCES users ON DELETE CASCADE,
  location_id BIGINT NOT NULL REFERENCES locations ON DELETE CASCADE,
  paid BOOLEAN,
  participant_limit INT DEFAULT 0,
  published TIMESTAMP WITHOUT TIME ZONE,
  request_moderation BOOLEAN DEFAULT true,
  state VARCHAR(50),
  title VARCHAR(120) NOT NULL
);

CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  event_id BIGINT NOT NULL REFERENCES events ON DELETE CASCADE,
  requester_id BIGINT NOT NULL REFERENCES users ON DELETE CASCADE,
  status VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS compilations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  pinned BOOLEAN,
  title VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS compilation_events (
  compilation_id BIGINT NOT NULL REFERENCES compilations ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES events ON DELETE CASCADE,
  PRIMARY KEY (compilation_id, event_id)
);

CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  text VARCHAR(1024) NOT NULL,
  author_id BIGINT NOT NULL REFERENCES users ON DELETE CASCADE,
  event_id BIGINT NOT NULL REFERENCES events ON DELETE CASCADE,
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  last_updated TIMESTAMP WITHOUT TIME ZONE NOT NULL
);